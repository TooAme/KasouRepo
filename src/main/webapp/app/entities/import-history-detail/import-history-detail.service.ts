import axios from 'axios';

import buildPaginationQueryOpts from '@/shared/sort/sorts';

import { type IImportHistoryDetail } from '@/shared/model/import-history-detail.model';

const baseApiUrl = 'api/import-history-details';

export default class ImportHistoryDetailService {
  public find(id: number): Promise<IImportHistoryDetail> {
    return new Promise<IImportHistoryDetail>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public retrieve(paginationQuery?: any): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .get(`${baseApiUrl}?${buildPaginationQueryOpts(paginationQuery)}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public delete(id: number): Promise<any> {
    return new Promise<any>((resolve, reject) => {
      axios
        .delete(`${baseApiUrl}/${id}`)
        .then(res => {
          resolve(res);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public create(entity: IImportHistoryDetail): Promise<IImportHistoryDetail> {
    return new Promise<IImportHistoryDetail>((resolve, reject) => {
      axios
        .post(`${baseApiUrl}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public update(entity: IImportHistoryDetail): Promise<IImportHistoryDetail> {
    return new Promise<IImportHistoryDetail>((resolve, reject) => {
      axios
        .put(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }

  public partialUpdate(entity: IImportHistoryDetail): Promise<IImportHistoryDetail> {
    return new Promise<IImportHistoryDetail>((resolve, reject) => {
      axios
        .patch(`${baseApiUrl}/${entity.id}`, entity)
        .then(res => {
          resolve(res.data);
        })
        .catch(err => {
          reject(err);
        });
    });
  }
}
